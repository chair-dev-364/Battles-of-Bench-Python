def stop_specific_sound(sound_name):
    """Stops all playing instances of a specific sound file (or streaming music)."""
    try:
        # Check if the currently streaming music matches the sound_name
        # Note: Pygame doesn't provide an easy way to get the *name* of the currently playing stream.
        # But stopping music is safe to do if the user requested to stop a known music track.
        # We can broadly stop music if we suspect it's playing that track (or just stop music if it's a music command).
        # We'll just stop the cached sound explicitly first.
        sound_file_mp3 = os.path.join(SOUNDS_DIR, f"{sound_name}.mp3")
        sound_file_wav = os.path.join(SOUNDS_DIR, f"{sound_name}.wav")
        music_file_mp3 = os.path.join(SOUNDS_DIR, 'Music', f"{sound_name}.mp3")
        music_file_wav = os.path.join(SOUNDS_DIR, 'Music', f"{sound_name}.wav")

        stopped_something = False
        with sound_cache_lock:
            for path in [sound_file_mp3, sound_file_wav, music_file_mp3, music_file_wav]:
                if path in sound_cache:
                    sound_cache[path].stop()
                    stopped_something = True

        # If it wasn't in cache, it might be the streaming track (which bypasses cache).
        # We'll just issue a mixer.music.stop() if we didn't find it in the cache,
        # assuming it might be the active stream.
        if not stopped_something:
             pygame.mixer.music.stop()
             
        print(f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] Stopped specific sound: {sound_name}")
    except Exception as e:
        print(f"Error stopping specific sound {sound_name}: {e}")

def _remove_metadata_with_ffmpeg(mp3_path):
    ffmpeg = shutil.which('ffmpeg')
    if not ffmpeg:
        return False
    tmp = mp3_path + '.nometa.tmp.mp3'
    cmd = [ffmpeg, '-loglevel', 'error', '-y', '-i', mp3_path, '-map', '0:a', '-c', 'copy', '-map_metadata', '-1', tmp]
    try:
        # Suppress window on Windows
        creationflags = 0
        if os.name == 'nt':
            creationflags = getattr(subprocess, 'CREATE_NO_WINDOW', 0x08000000)
        subprocess.run(cmd, check=True, creationflags=creationflags)
        # Replace original file atomically
        try:
            os.replace(tmp, mp3_path)
        except Exception:
            # Fallback: try remove and rename
            if os.path.exists(mp3_path): os.remove(mp3_path)
            os.rename(tmp, mp3_path)
        print(f"[TagFix] Removed metadata from {os.path.basename(mp3_path)} using ffmpeg.")
        return True
    except Exception as e:
        try:
            if os.path.exists(tmp): os.remove(tmp)
        except Exception:
            pass
        print(f"[TagFix] ffmpeg metadata removal failed for {mp3_path}: {e}")
        return False


def clean_id3_tags(mp3_path):
    """Try to detect and fix malformed ID3 tags that produce BOM warnings.

    Strategy:
      1) Try mutagen to load ID3 tags; if it loads, assume OK.
      2) If mutagen raises, try to strip metadata using ffmpeg (if available).
      3) Return True if we modified the file, False otherwise.
    """
    try:
        from mutagen.id3 import ID3
        try:
            ID3(mp3_path)
            return False  # tags present and readable
        except Exception:
            # Attempt to remove all ID3 tags using mutagen if possible
            try:
                from mutagen.id3 import ID3, Frames, ID3NoHeaderError
                try:
                    id3 = ID3()
                    id3.save(mp3_path)
                except Exception:
                    pass
            except Exception:
                pass
    except Exception:
        # mutagen not available; proceed to ffmpeg attempt
        pass

    # Try ffmpeg fallback to strip metadata if available
    try:
        return _remove_metadata_with_ffmpeg(mp3_path)
    except Exception:
        return False


